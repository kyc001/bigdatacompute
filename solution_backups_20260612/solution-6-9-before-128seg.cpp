    static float clip_score(float score) {
        return std::min(5.0f, std::max(0.5f, score));
    }

    void precompute_user_factor() {
        for (int user = 0; user < users; ++user) {
            const int hi = static_cast<int>((1LL * user * factor_high) / users);
            const int lo = user % factor_low;
            float value = base_intercept;
            for (int r = 0; r < factor_rank; ++r) {
                value += factor_a[hi][r] * factor_b[lo][r];
            }
            user_factor_score[user] = value;
        }
    }

    void precompute_count_luts() {
        user_count_term.resize(count_lut_limit + 1);
        user_sum_weight.resize(count_lut_limit + 1);
        item_count_term.resize(count_lut_limit + 1);
        item_sum_weight.resize(count_lut_limit + 1);
        for (int count = 0; count <= count_lut_limit; ++count) {
            const float c = static_cast<float>(count);
            const float lu = std::log1p(c);
            user_count_term[count] = coef[1] * lu
                                   + coef[3] * lu * lu
                                   + coef[5] / std::sqrt(c + 1.0f);
            const float li = lu;
            item_count_term[count] = coef[2] * li
                                   + coef[4] * li * li
                                   + coef[6] / std::sqrt(c + 1.0f);
            float uw = 0.0f;
            float iw = 0.0f;
            if (count > 0) {
                for (int idx = 0; idx < 10; ++idx) {
                    uw += coef[7 + idx] / (c + user_shrinks[idx]);
                    iw += coef[17 + idx] / (c + item_shrinks[idx]);
                }
            }
            user_sum_weight[count] = uw;
            item_sum_weight[count] = iw;
        }
    }

    float user_score_from_stats(float sum, int count) const {
        if (count <= count_lut_limit) {
            return user_count_term[count] + sum * user_sum_weight[count];
        }
        const float c = static_cast<float>(count);
        const float log_count = std::log1p(c);
        float score = coef[1] * log_count
                    + coef[3] * log_count * log_count
                    + coef[5] / std::sqrt(c + 1.0f);
        for (int idx = 0; idx < 10; ++idx) {
            score += coef[7 + idx] * sum / (c + user_shrinks[idx]);
        }
        return score;
    }

    float item_score_from_stats(float sum, int count) const {
        if (count <= count_lut_limit) {
            return item_count_term[count] + sum * item_sum_weight[count];
        }
        const float c = static_cast<float>(count);
        const float log_count = std::log1p(c);
        float score = coef[2] * log_count
                    + coef[4] * log_count * log_count
                    + coef[6] / std::sqrt(c + 1.0f);
        for (int idx = 0; idx < 10; ++idx) {
            score += coef[17 + idx] * sum / (c + item_shrinks[idx]);
        }
        return score;
    }

    void rebuild_scores() {
        if (!has_updates) {
            scores_ready = true;
            return;
        }

        if (use_factorized_model) {
            for (int user = 0; user < users; ++user) {
                const int count = user_count[user];
                const float sum = user_sum[user];
                const float score = user_score_from_stats(sum, count);
                user_factor_score[user] += score;
            }

            for (int item = 0; item < items; ++item) {
                const int count = item_count[item];
                const float sum = item_sum[item];
                item_base_score[item] = item_score_from_stats(sum, count);
            }
        } else {
            for (int user = 0; user < users; ++user) {
                const float count = static_cast<float>(user_count[user]);
                user_factor_score[user] = count > 0.0f ? 0.8f * user_sum[user] / (count + 5.0f) : 0.0f;
            }
            for (int item = 0; item < items; ++item) {
                const float count = static_cast<float>(item_count[item]);
                item_base_score[item] = count > 0.0f ? 0.9f * item_sum[item] / (count + 3.0f) : 0.0f;
            }
        }

        scores_ready = true;
    }
